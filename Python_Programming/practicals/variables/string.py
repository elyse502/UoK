university_name = "University of Kigali"
print(type(university_name))
# print the string length
print(len(university_name))
# string slicing
print(university_name[0:9])  # slice the string from 0 to 9
print(university_name[:9])
print(university_name[2:8])
print(university_name[3:])
print(university_name[3:9])

# Slicing in reverse direction
print(university_name[-5:-2])

